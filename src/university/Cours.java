    private void Quick() {
        if(jTextField1.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Course Number field is empty" , "Empty",2);
            }else{
            
                                        try {
                                          int nr = Integer.parseInt(jTextField1.getText());
                                       } catch (NumberFormatException e) {
                                           JOptionPane.showMessageDialog(this, "Please Enter an Integer Number !");
                                       }
                try {
                     
                    con = ConnectionDataBase.connect();
                    com = con.prepareStatement("select * from Course where idCourse = '"+Integer.parseInt(jTextField1.getText())+"'");
                    rst = com.executeQuery();
                    
                    if(rst.next()){
                        jTextField1.setEnabled(false);
                        
                        
                        jTextField2.setText(rst.getString(2));
                        jTextField2.setEnabled(true);
                        
                        jTextField3.setText(rst.getString(3));
                        jTextField3.setEnabled(true);
                        
                        String ProfN = rst.getString(4);
                        jComboBox1.setEnabled(true);
                        if(ProfN.equals(" ")){
                            jComboBox1.setSelectedIndex(-1);
                        }else{
                            jComboBox1.setSelectedItem(ProfN);
                        }
                        
                        
                        
                        jTextField4.setText(rst.getString(5));
                        jTextField4.setEnabled(true);
                        
                        int semester = Integer.parseInt(rst.getString(6));
                      
                            jRadioButton1.setSelected(true);
                            
                        if(semester == 2){
                            jRadioButton2.setSelected(true);
                        }
                        
                        jRadioButton1.setEnabled(true);
                        jRadioButton2.setEnabled(true);
                        
                        
                    }else{
                            JOptionPane.showMessageDialog(null,"Course Number is Not Exist ", "Not Exist",2); 
                    }
                } catch (SQLException ex) {
                    
                } finally{
                    try {
                        con.close();
                    } catch (SQLException ex) {
                    }
                }
        }

    }
