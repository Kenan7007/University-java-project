    private void Insert() {
         String Err = "You forgot to enter the following data :"+"\n\n";
            
            if(jTextField1.getText().equals("")){
                Err += "Course Number\n";
            }
            if(jTextField2.getText().equals("")){
                Err += "Course Name\n";
            }
            if(jTextField3.getText().equals("")){
                Err += "Hours Number\n";
            }
            if(jTextField4.getText().equals("")){
                Err += "Price for one Hour\n";
            }
            
            if(jComboBox1.getSelectedIndex() == -1){
                Err += "Doctor of This course\n";
            }
            
            if(!jRadioButton1.isSelected()&&!jRadioButton2.isSelected()){
                Err += "Semester \n";
            }
            
            int semester = 1;
            if(jRadioButton2.isSelected()){
                semester = 2;
            }
            
            
            
             if(!jTextField1.getText().equals("") &&
               !jTextField2.getText().equals("") &&
               !jTextField3.getText().equals("")&&
               !jTextField4.getText().equals("")&&
               jComboBox1.getSelectedIndex() != -1 &&
               (jRadioButton1.isSelected() || jRadioButton2.isSelected())){
                  
                                       try {
                                          int nr = Integer.parseInt(jTextField1.getText());
                                       } catch (NumberFormatException e) {
                                           JOptionPane.showMessageDialog(this, "Please Enter an Integer Number !");
                                       }
                                       
                                       
                 try {
              
                                       
                 con = ConnectionDataBase.connect();
                 com = con.prepareStatement("select*from Course where idCourse = '"+Integer.parseInt(jTextField1.getText())+"'");
                 rst = com.executeQuery();
                 if(!rst.next()){
                      com = con.prepareStatement("insert into Course values('"+Integer.parseInt(jTextField1.getText())+"','"
                                                                                 +jTextField2.getText()+"','"
                                                                                 +Integer.parseInt(jTextField3.getText())+"','"
                                                                                 +jComboBox1.getSelectedItem()+"','"
                                                                                 +Integer.parseInt(jTextField4.getText())+"','"
                                                                                 +semester+"')");

                        com.execute();
                        
                        JOptionPane.showMessageDialog(null,"The add process has been completed","Course has been added",JOptionPane.INFORMATION_MESSAGE);
                        Clear();
                        
                        
                 }else{
                        JOptionPane.showMessageDialog(null,"Course Number is Already Exist ", "ADD FAILED!",2);
                 }
                 
                 
             } catch (SQLException ex) {
             }finally{
                 try {
                     con.close();
                 } catch (SQLException ex) {
                 }
             }
                 
                 
            
          
            }else{     
              JOptionPane.showMessageDialog(null,Err, "Data Lost",2);
            } 
    }        

